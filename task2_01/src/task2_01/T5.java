package task2_01;

public class T5 {

	public static void main(String[] args) {
		//random 
	System.out.println((int)(Math.random() * 100)); //0~100 랜덤출력
		//abs
	System.out.println(Math.abs(-10));   //절댓값 출력
		//pow
	System.out.println((int)Math.pow(5, 2)); // 5의 2제곱근
		//sqrt
	System.out.println((int)Math.sqrt(25));  // 25의 루틋값
		//ceil
	System.out.println(Math.ceil(10.1));      //올림
		//round
	System.out.println(Math.round(10.4));     //반올림
		//floor
	System.out.println(Math.floor(10.9));     //내림
	}

}
